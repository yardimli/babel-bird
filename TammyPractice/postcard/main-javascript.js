//After loading the whole page, the codes below will run-
$(document).ready(function () {
    UpdatePostcard(3);


//for the photo select dropdown list to be shown --
    $('#photoListContainer').on('click', function (event) {
        $("#photoList").toggleClass('photoList');
        return false;
    });

//for the photo selecg dropdwon list to be hidden (the class photolist has a display:none;
    //for hiding the list-
    $(document).click(,function () {
        $("#photoList").addClass('photoList');
    });//

    // for hiding the form
    $("#form").on('mouseenter', function () {
        $(this).css("opacity", "1");
    });

    $("#form").on('mouseleave', function () {
        $(this).css("opacity", "0.1");
    });


    // $('#consignee').click(function (){
    //     $(this).html();
    //
    // });


    // $('#consignee').click(function () {
    //     $('#consignee').remove(value);
    // });
//when the user type anything, any keyup, tht content will be updated to the page
    $('#consignee').on('keyup', function () {
        $('.consignee').html($('#consignee').val());
    });

    $('#address').on('keyup', function () {
        $('.address').html($('#address').val());
    });

    $('#content').on('keyup', function () {
        $('.content').html($('#content').val());
    });

    $('#from').on('keyup', function () {
        $('.from').html($('#from').val());
    });


//after click the preview button, the preview page will see the chosen photo and the
//written content in the form-
    $('.previewBtn').on('click',function () {

        var from =   $('#from').val();
        var consignee = $('#consignee').val();
        var address = $('#address').val();
        var content = $('#content').val();




        var win = window.open("https://local.elosoft.tw/Postcard/preview.html?from=" + from +
            "&consignee=" + consignee +
            "&address=" + address +
            "&content=" + content +
            "&postcard_background=" + LastSelectedPicture, '_blank');
            win.focus();

    });

//for reloading the page
    $('.resetBtn').click(function () {
        document.location.reload(true);
    });


//----------------------------------------------------------------------------------------------------------------------
//This for loop is for giving #photolist list items (the pictures that is inside array Pictures
    for (var counter=1; counter<Pictures.length; counter++) {
        $("#photoList").append("<li class=\"submenu\" data-imagenumber='" + (Pictures.length-counter) + "'>" + Pictures[(Pictures.length-counter)].name + "</li>\n");
    }

//after clicking on any .submenu list item, the text of that list item will be assigned to #photomenu
    $(".submenu").on('click', function () {
        $("#photomenu").text($(this).text()); //#photomenu will show the selected photo name
        UpdatePostcard(parseInt($(this).data("imagenumber")));  //after user select the photo, know which one the user chose
        LastSelectedPicture = $(this).data("imagenumber");
        $("#indexHeader").remove();
    })


});
