//----------------------------------------------------------------------------------------------------------------------
// This is the function for telling the program after getting the selected picture, all the
// targeted divs need to add the CSS styles that are defined within the Pictures array-

function UpdatePostcard(ShowPictureNumber) {

    // console.log(Pictures[ShowPictureNumber].image);
    // console.log(Pictures[ShowPictureNumber]["background-Color"]);
    $("#postcard").css({backgroundColor: Pictures[ShowPictureNumber].backgroundColor});
    $("#postcard").css({'background-image': 'url("./' + PhotoFolder + '/' + Pictures[ShowPictureNumber].image + '")'});
    $("#postcard").css({'background-position': Pictures[ShowPictureNumber].position});


    $(".consignee").css({'background-color': Pictures[ShowPictureNumber]["textBackground-Consignee"]});
    $(".consignee").css({'color': Pictures[ShowPictureNumber]["textColor-Consignee"]});
    $(".consignee").css({'top': Pictures[ShowPictureNumber].consigneeDesktopTop});
    $(".consignee").css({'left': Pictures[ShowPictureNumber].consigneeDesktopLeft});

    $(".address").css({'background-color': Pictures[ShowPictureNumber].textBackgroundAddress});
    $(".address").css({'color': Pictures[ShowPictureNumber].textColorAddress});
    $(".address").css({'top': Pictures[ShowPictureNumber].addressDesktopTop});
    $(".address").css({'left': Pictures[ShowPictureNumber].addressDesktopLeft});

    $(".content").css({'background-color': Pictures[ShowPictureNumber]["textBackground-Content"]});
    $(".content").css({'color': Pictures[ShowPictureNumber]["textColor-Content"]});
    $(".content").css({'top': Pictures[ShowPictureNumber].contentDesktopTop});
    $(".content").css({'left': Pictures[ShowPictureNumber].contentDesktopLeft});

    $(".from").css({'background-color': Pictures[ShowPictureNumber].textBackgroundFrom});
    $(".from").css({'color': Pictures[ShowPictureNumber].textColorFrom});
    $(".from").css({'top': Pictures[ShowPictureNumber].fromDesktopTop});
    $(".from").css({'left': Pictures[ShowPictureNumber].fromDesktopLeft});

}





